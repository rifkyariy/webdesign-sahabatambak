    public function getPonds(Request $request)
    {
        if ($request->ajax()) {
            $data = Pond::latest()->get();
            return Datatables::of($data)
                ->addIndexColumn()
                ->addColumn('action', function($row){
                    $actionBtn = '<a href="list/'.$row->id.'" class="detail btn btn-primary btn-sm">Detail</a> <a href="javascript:void(0)" class="delete btn btn-danger btn-sm">Delete</a>';
                    return $actionBtn;
                })
                ->rawColumns(['action'])
                ->make(true);
        }
    }
